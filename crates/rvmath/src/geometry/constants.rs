//! High-precision geometric constants.

/// π (Pi) - Ratio of circle circumference to diameter
pub const PI: f64 = std::f64::consts::PI;

/// π/2 - Half of π, commonly used in trigonometry
pub const PI_2: f64 = std::f64::consts::FRAC_PI_2;

/// π/4 - Quarter of π
pub const PI_4: f64 = std::f64::consts::FRAC_PI_4;

/// 2π - Double of π, used in full circle calculations
pub const TAU: f64 = std::f64::consts::TAU;

/// Alias for TAU
pub const TWO_PI: f64 = TAU;

/// φ (Phi) - Golden ratio (1 + √5) / 2 ≈ 1.618...
///
/// Used in aesthetics, biology, and architecture.
pub const GOLDEN_RATIO: f64 = 1.618033988749895;

/// √2 (Square root of 2) ≈ 1.414...
///
/// Ratio of diagonal to side of a square.
pub const SQRT_2: f64 = std::f64::consts::SQRT_2;

/// √3 (Square root of 3) ≈ 1.732...
///
/// Height of equilateral triangle with side 2.
pub const SQRT_3: f64 = 1.732050807568877;

/// √5 (Square root of 5) ≈ 2.236...
///
/// Related to golden ratio and pentagons.
pub const SQRT_5: f64 = 2.23606797749979;

/// 1/π
pub const INV_PI: f64 = std::f64::consts::FRAC_1_PI;

/// 2/π
pub const TWO_OVER_PI: f64 = std::f64::consts::FRAC_2_PI;

/// 1/√2
pub const INV_SQRT_2: f64 = std::f64::consts::FRAC_1_SQRT_2;

/// 2/√π
pub const TWO_OVER_SQRT_PI: f64 = std::f64::consts::FRAC_2_SQRT_PI;


